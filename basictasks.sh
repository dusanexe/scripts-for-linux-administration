#!/bin/bash
######## day_base creator for morning info of server! #########
#######################################################################################
######################              author: Dushan               ######################
#######################################################################################

# this script run basic morning tasks to check servers
# status and create local base with file of day when it is run

########################################
############ start script ##############
########################################

mkdir ./day_base
(echo
echo '---------------------------------------------------------------------------------' 
top | head -15
echo '---------------------------------------------------------------------------------' 
echo
echo '* memory status:'
echo
free -h
echo '---------------------------------------------------------------------------------'  
echo '---------------------------------------------------------------------------------' 
echo
echo '* system run time:'
echo
uptime
echo '---------------------------------------------------------------------------------'
echo '---------------------------------------------------------------------------------'
echo
echo '* current active users:'
echo
users
echo '---------------------------------------------------------------------------------' 
echo '---------------------------------------------------------------------------------'
date
echo
echo
echo '* day_base size:' 
du -h) >> ./day_base/$(date +%d_%m_%Y)
echo '#################################################################################' 
echo '##################### successfully created file in base #########################'
echo '#################################################################################'
